// Obtener elementos del DOM
const searchInput = document.getElementById('searchInput');
const resultsContainer = document.getElementById('results');

// FunciÃ³n para buscar figuras de acciÃ³n
function searchFiguras() {
  // Obtener el valor del input de bÃºsqueda
  const searchTerm = searchInput.value.toLowerCase();

  // Verificar si el tÃ©rmino de bÃºsqueda estÃ¡ vacÃ­o
  if (searchTerm.trim() === '') {
    // Limpiar el contenedor de resultados
    resultsContainer.innerHTML = '';
    return;
  }

  // Filtrar la matriz basada en el tÃ©rmino de bÃºsqueda
  const filteredFiguras = todofunco.filter(figura =>
    figura.nombre.toLowerCase().includes(searchTerm) ||
    figura.descripcion.toLowerCase().includes(searchTerm)
  );

  // Mostrar los resultados en el contenedor
  displayResults(filteredFiguras);
}

// FunciÃ³n para mostrar los resultados en el contenedor
function displayResults(figuras) {
  // Limpiar el contenedor de resultados
  resultsContainer.innerHTML = '';

  // Mostrar los resultados
  if (figuras.length > 0) {
    figuras.forEach(figura => {
      const figuraElement = document.createElement('div');
      figuraElement.classList.add('figura');
      figuraElement.innerHTML = `
        <img src="${figura.img}" alt="${figura.nombre}">
        <h3>${figura.nombre}</h3>
        <p>${figura.descripcion}</p>
        <p>Precio: ${figura.Precio}</p>
      `;

      const button = document.createElement('button');
      button.textContent = 'Ver detalles';
      button.addEventListener('click', function() {
        // Cambiar el enlace a la URL deseada para cada figura
        const enlace = figura.enlace; // Reemplaza con el enlace correspondiente a la figura

        // Redirigir a la URL especificada
        window.location.href = enlace;
      });

      figuraElement.appendChild(button);
      resultsContainer.appendChild(figuraElement);
    });
  } else {
    resultsContainer.textContent = 'No se encontraron resultados.';
  }
}

// Event listener para el input de bÃºsqueda
searchInput.addEventListener('input', searchFiguras);